from __future__ import annotations

"""
Code Atlas Benchmark Suite

This script performs a comprehensive performance evaluation of the Code Atlas system.
It measures indexing speed (Full vs Incremental), graph density, and query latency
across a variety of repository scales.
"""

import json
import shutil
import subprocess
import os
from dataclasses import dataclass
from pathlib import Path
from time import perf_counter

from code_atlas.core.cache import DEFAULT_CACHE_PATH
from code_atlas.core.indexing import build_graph
from code_atlas.discovery.traversal import impact_of

@dataclass
class BenchResult:
    repo_name: str
    language: str
    total_files: int
    full_index_time: float
    inc_index_time: float
    graph_size_kb: float
    query_latency_ms: float
    nodes: int
    edges: int

# Targeted repositories for different scales (Small -> Huge)
REPOS = [
    ("pallets/flask", "https://github.com/pallets/flask.git", "python", "flask.app:Flask"),
    ("axios/axios", "https://github.com/axios/axios.git", "typescript", "axios:Axios"),
    ("django/django", "https://github.com/django/django.git", "python", "django.db.models:Model"),
    ("facebook/react", "https://github.com/facebook/react.git", "typescript", "react:useState"),
    ("kubernetes/kubernetes", "https://github.com/kubernetes/kubernetes.git", "go", "k8s.io.kubernetes.pkg.kubelet:Kubelet"),
]

def main() -> None:
    root = Path(__file__).resolve().parents[1]
    bench_root = root / "tmp" / "bench_suite"
    repos_dir = bench_root / "repos"
    graphs_dir = bench_root / "graphs"
    
    repos_dir.mkdir(parents=True, exist_ok=True)
    graphs_dir.mkdir(parents=True, exist_ok=True)

    results: list[BenchResult] = []

    for slug, url, lang, test_symbol in REPOS:
        print(f"--- Benchmarking {slug} ({lang}) ---")
        try:
            local_repo = ensure_clone(repos_dir, slug, url)
            clear_cache(local_repo)

            # 1. Full Index
            t0 = perf_counter()
            full_res = build_graph(local_repo)
            full_time = perf_counter() - t0
            
            graph_path = graphs_dir / f"{slug.replace('/', '_')}.json"
            full_res.graph.write_json(graph_path)
            graph_size = os.path.getsize(graph_path) / 1024
            stats = full_res.graph.stats()

            # 2. Incremental Index (No changes)
            t1 = perf_counter()
            build_graph(local_repo)
            inc_time = perf_counter() - t1

            # 3. Query Latency (Impact Analysis)
            # We try to find the symbol in the graph first
            found_ids = [n.id for n in full_res.graph.nodes.values() if test_symbol in n.id or test_symbol in n.name]
            query_latency = 0.0
            if found_ids:
                t2 = perf_counter()
                impact_of(full_res.graph, found_ids[0], depth=3)
                query_latency = (perf_counter() - t2) * 1000

            results.append(BenchResult(
                repo_name=slug,
                language=lang,
                total_files=full_res.scanned_files,
                full_index_time=full_time,
                inc_index_time=inc_time,
                graph_size_kb=graph_size,
                query_latency_ms=query_latency,
                nodes=stats.get("nodes", 0),
                edges=stats.get("edges", 0)
            ))
            print(f"Done. Full: {full_time:.2f}s, Inc: {inc_time:.2f}s, Query: {query_latency:.1f}ms")

        except Exception as e:
            print(f"Failed to benchmark {slug}: {e}")

    # Generate Report
    report = generate_markdown(results)
    (root / "docs" / "benchmarks.md").write_text(report, encoding="utf-8")
    print(f"\nReport generated at docs/benchmarks.md")

def ensure_clone(repos_dir: Path, slug: str, url: str) -> Path:
    local_path = repos_dir / slug.replace("/", "__")
    if not local_path.exists():
        print(f"Cloning {url}...")
        subprocess.run(["git", "clone", "--depth", "1", url, str(local_path)], check=True, capture_output=True)
    return local_path

def clear_cache(repo_path: Path) -> None:
    cache_file = repo_path / DEFAULT_CACHE_PATH
    if cache_file.exists():
        cache_file.unlink()

def generate_markdown(results: list[BenchResult]) -> str:
    lines = [
        "# Code Atlas Performance Benchmarks",
        "",
        "This report is automatically generated by `scripts/benchmark_suite.py`.",
        "",
        "## Indexing & Storage Performance",
        "Comparison of cold-start indexing vs. incremental re-indexing, and resulting graph size.",
        "",
        "| Repository | Language | Files | Full Index | Incremental | Speedup | Graph Size |",
        "| :--- | :--- | :--- | :--- | :--- | :--- | :--- |"
    ]
    
    for r in results:
        speedup = r.full_index_time / r.inc_index_time if r.inc_index_time > 0 else 0
        lines.append(f"| `{r.repo_name}` | {r.language} | {r.total_files} | {r.full_index_time:.2f}s | {r.inc_index_time:.2f}s | {speedup:.1f}x | {r.graph_size_kb:.1f} KB |")

    lines.extend([
        "",
        "## Query & Graph Complexity",
        "Measurements of internal graph density and response time for complex symbolic queries.",
        "",
        "| Repository | Nodes | Edges | Density | Impact Query (D3) |",
        "| :--- | :--- | :--- | :--- | :--- |"
    ])

    for r in results:
        density = r.edges / r.nodes if r.nodes > 0 else 0
        lines.append(f"| `{r.repo_name}` | {r.nodes} | {r.edges} | {density:.2f} | {r.query_latency_ms:.1f} ms |")

    lines.extend([
        "",
        "---",
        "*Benchmarks performed on: macOS / Apple Silicon.*"
    ])
    
    return "\n".join(lines)

if __name__ == "__main__":
    main()
